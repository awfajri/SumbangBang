    private void btnBackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBackMouseClicked
        if (currentUser.getRole().equalsIgnoreCase("DONATUR")) {
            new DashboardDonatur(currentUser).setVisible(true);
        } else if (currentUser.getRole().equalsIgnoreCase("PENERIMA")) {
            new DashboardPenerima(currentUser).setVisible(true);
        } else {
            // Jaga-jaga kalau Admin
            new DashboardAdmin(currentUser).setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnBackMouseClicked

