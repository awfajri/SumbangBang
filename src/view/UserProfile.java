    private void setupProfileData() {
        if (currentUser != null) {
            // 1. Isi Data Dasar
            txtName.setText(currentUser.getName());
            txtEmail.setText(currentUser.getEmail());
            txtPhone.setText(currentUser.getPhone());
            txtAddress.setText(currentUser.getAddress());
            
            // 2. Logika Deteksi Role yang Lebih Detail
            String roleDisplay = currentUser.getRole();
            
            if (currentUser instanceof Donatur) {
                Donatur d = (Donatur) currentUser;
                if (d.getBusinessType() != null) {
                    roleDisplay += " - " + d.getBusinessType(); // Contoh: "DONATUR - Restoran"
                }
            } else if (currentUser instanceof Penerima) {
                Penerima p = (Penerima) currentUser;
                if (p.getRecipientType() != null) {
                    roleDisplay += " - " + p.getRecipientType(); // Contoh: "PENERIMA - Panti Asuhan"
                }
            }
            
            txtRole.setText(roleDisplay);
        }
    }

